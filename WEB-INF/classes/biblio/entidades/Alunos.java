package biblio.entidades;

public class Alunos {
	private int codmatricula;
	private String nomealu;
	private String enderecoalu;
	private String situacaoalu;


	public int getCodmatricula() {
		return codmatricula;
	}
	public void setCodmatricula(int codmatricula) {
		this.codmatricula = codmatricula;
	}
	public String getNomealu() {
		return nomealu;
	}
	public void setNomealu(String nomealu) {
		this.nomealu = nomealu;
	}
	public String getEnderecoalu() {
		return enderecoalu;
	}
	public void setEnderecoalu(String enderecoalu) {
		this.enderecoalu = enderecoalu;
	}
	public String getSituacaoalu() {
		return situacaoalu;
	}
	public void setSituacaoalu(String situacaoalu) {
		this.situacaoalu = situacaoalu;
	}



}
