package biblio.entidades;

public class Biblioteca {
	private int codbib;
	private String nomebib;
	private String enderecobib;
	
	public int getCodbib() {
		return codbib;
	}
	public void setCodbib(int codbib) {
		this.codbib = codbib;
	}
	public String getNomebib() {
		return nomebib;
	}
	public void setNomebib(String nomebib) {
		this.nomebib = nomebib;
	}
	public String getEnderecobib() {
		return enderecobib;
	}
	public void setEnderecobib(String enderecobib) {
		this.enderecobib = enderecobib;
	}
	
	

}
